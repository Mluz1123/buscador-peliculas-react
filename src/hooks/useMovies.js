import { useRef, useState, useMemo, useCallback } from 'react'
import { searchMovies } from '../service/movies'

export function useMovies({ search, sort }) {
  const [movies, setMovies] = useState([])
  const [loading, setLoading] = useState(false)
  const [error, setError] = useState(null)
  const previousSearch = useRef(search)

  //useCallback esta pensado solo para las funciones y sinplifica la sintasis
  //por debajo utiliza el useMemo lo cual realizan el mismo proceso

  const getMovies = useCallback(async ({ search }) => {
    if (search === previousSearch.current) return
    try {
      setLoading(true)
      setError(null)
      previousSearch.current = search
      const newMovies = await searchMovies({ search })
      setMovies(newMovies)
    } catch (e) {
      setError(e.message)
    } finally {
      setLoading(false)
    }
  }, [])
  //El useMemo memoriza computaciones que no queremos que se vuelvan a renderizar
  //a no ser que queremos que se cambien las dependencias.
  const sortedMovies = useMemo(() => {
    return sort
      ? [...movies].sort((a, b) => a.title.localeCompare(b.title))
      : movies
  }, [sort, movies])

  return { movies: sortedMovies, getMovies, loading }
}
